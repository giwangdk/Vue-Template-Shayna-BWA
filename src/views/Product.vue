<template>
    <div class="product">
        <HeaderShayna />
        <!-- Breadcrumb Section Begin -->
        <div class="breacrumb-section text-left">
        <div class="container">
            <div class="row">
            <div class="col-lg-12">
                <div class="breadcrumb-text product-more">
                <router-link to="/"><i class="fa fa-home"></i> Home</router-link>
                <span>Detail</span>
                </div>
            </div>
            </div>
        </div>
        </div>
        <!-- Breadcrumb Section Begin -->

        <!-- Product Shop Section Begin -->
        <section class="product-shop spad page-details">
        <div class="container">
            <div class="row">
            <div class="col-lg-12">
                <div class="row">
                <div class="col-lg-6">
                    <div class="product-pic-zoom">
                    <img class="product-big-img" :src="photo_default" alt="" />
                    </div>
                    <div class="product-thumbs" v-if="productDetails.galleries.length > 0">
                    <carousel  :autoplay="true" :nav="false" :dots="false">
                        <div 
                        v-for="ss in productDetails.galleries"
                        :key="ss.id"
                        class="pt" @click="changeImage(ss.photo)" 
                        :class="ss.photo == photo_default ? 'active' : ''">
                        <img :src="ss.photo" alt="" />
                        </div>
                    </carousel>
                    </div>
                </div>
                <div class="col-lg-6">
                    <div class="product-details text-left">
                    <div class="pd-title">
                        <span>{{productDetails.type}}</span>
                        <h3>{{productDetails.name}}</h3>
                    </div>
                    <div class="pd-desc">
                        <p>
                        {{productDetails.description}}
                        </p>
                        <h4>${{productDetails.price}}</h4>
                    </div>
                    <div class="quantity">
                        <router-link to="/cart" class="primary-btn pd-cart"
                        >Add To Cart</router-link
                        >
                    </div>
                    </div>
                </div>
                </div>
            </div>
            </div>
        </div>
        </section>
        <!-- Product Shop Section End -->
        <RelatedProduct/>
        <Footer />
    </div>
</template>

<script>
// // @ is an alias to /src

import HeaderShayna from "@/components/HeaderShayna.vue";
import Footer from "@/components/Footer.vue";
import RelatedProduct from "@/components/RelatedProduct.vue";
import carousel from 'vue-owl-carousel';
import axios from 'axios';

export default {
    name: "product",
    components: {
        HeaderShayna,
        Footer,
        carousel,
        RelatedProduct
    },
    data(){
        return{
            photo_default:"",
            productDetails : []
        }
    },
    
    methods:{
        changeImage(urlImage){
            this.photo_default = urlImage
        },
        setDataPicture(data){
        this.productDetails = data;

        this.photo_default = data.galleries[0].photo
    },
    },
      mounted(){
        axios
        .get("http://127.0.0.1:8000/api/products", {
            params: {
                id:this.$route.params.id
            }
        })
        .then(res=>(this.setDataPicture(res.data.data)))
        .catch(err=>console.log(err));
        
    }
};
</script>

